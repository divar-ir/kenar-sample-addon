def is_valid_postal_code_for_city(city: int, postal_code: str):
    if city not in city_postal_codes:
        return True
    city_range = city_postal_codes[city]
    first_5_digits = int(postal_code[:5])
    start, end = city_range[0], city_range[1]
    if start <= first_5_digits <= end:
        return True

    return False
